package day02.practice;




 public class Logger {
    public static String debug(String msg) {
        return "DEBUG: " + msg;
    }

    public static String info(String msg) {
        return "INFO: " + msg;
    }

    public static String error(String msg) {
        return "ERROR: " + msg;
    }

    public static String debug(int num) {
        return "DEBUG: " + num;
    }

    public static String info(int num) {
        return "INFO: " + num;
    }

    public static String error(int num) {
        return "ERROR: " + num;
    }

    public static String debug(double num) {
        return "DEBUG: " + num;
    }

    public static String info(double num) {
        return "INFO: " + num;
    }

    public static String error(double num) {
        return "ERROR: " + num;
    }

    public static String debug(boolean value) {
        return "DEBUG: " + value;
    }

    public static String info(boolean value) {
        return "INFO: " + value;
    }

    public static String error(boolean value) {
        return "ERROR: " + value;
    }
}



